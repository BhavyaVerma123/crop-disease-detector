def get_disease_info(label: str) -> dict:
    """Parse a HuggingFace model label and return disease info."""
    # Normalize the label
    normalized = label.strip()

    # Direct lookup first
    if normalized in DISEASE_DATABASE:
        return DISEASE_DATABASE[normalized]

    # Try case-insensitive and partial matching
    for key, value in DISEASE_DATABASE.items():
        if key.lower() == normalized.lower():
            return value

    # Fuzzy fallback: match plant and disease keywords
    for key, value in DISEASE_DATABASE.items():
        plant = value["plant"].lower()
        disease = value["disease"].lower()
        label_lower = normalized.lower().replace("_", " ")
        if plant in label_lower or disease in label_lower:
            return value

    # Generic fallback
    parts = normalized.split("___") if "___" in normalized else normalized.split("_")
    plant_name = parts[0].replace("_", " ").title() if parts else "Unknown Plant"
    disease_name = parts[1].replace("_", " ").title() if len(parts) > 1 else normalized

    return {
        "display_name": f"{plant_name} – {disease_name}",
        "plant": plant_name,
        "disease": disease_name,
        "is_healthy": "healthy" in normalized.lower(),
        "severity": "Unknown",
        "severity_color": "gray",
        "description": f"Detected condition: {normalized.replace('_', ' ')}.",
        "treatment": "Consult a local agricultural extension service for treatment recommendations.",
        "prevention": "Practice good crop hygiene, crop rotation, and regular scouting."
    }
